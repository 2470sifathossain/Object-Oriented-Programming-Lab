package AddSubbMul;


public class Arithmetic {
    
    
   static int Add(int a , int b)
       
   {
       return a+b;
   }
   
   static int Sub(int a , int b)
       
   {
       return a-b;
   }
   
   static int Mul(int a , int b)
       
   {
       return a*b;
   }
   
    
}
